package com.star.cinema.movie.dto;

public class GradeDTO {
	private int gradeNum;
	private int grade;
	private String review;
	private int likeRate;
	private String id;
	private int movieListNum;
	
	public int getGradeNum() {
		return gradeNum;
	}
	public void setGradeNum(int gradeNum) {
		this.gradeNum = gradeNum;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public String getReview() {
		return review;
	}
	public void setReview(String review) {
		this.review = review;
	}
	public int getLikeRate() {
		return likeRate;
	}
	public void setLikeRate(int likeRate) {
		this.likeRate = likeRate;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getMovieListNum() {
		return movieListNum;
	}
	public void setMovieListNum(int movieListNum) {
		this.movieListNum = movieListNum;
	}
	
	
}
